def add(x,y):
    print("Given numbers are {} and {} and and its addition is {}".format(x,y,(x+y)))
def sub(x,y):
    print("Given numbers are {} and {} and and its subtraction is {}".format(x,y,(x-y)))
def multiply(x,y):
    print("Given numbers are {} and {} and and its multiplication is {}".format(x,y,(x*y)))
def divide(x,y):
    print("Given numbers are {} and {} and and its division is {}".format(x,y,(x/y)))
def remainder(x,y):
    print("Given numbers are {} and {} and and its remainder is {}".format(x,y,(x%y)))

